nombre = 'Victor'
edad = 25

# str.format()
print("Hola, soy % s, y tengo % s años."%(nombre, edad))
# .format %
print("Hola que tal, soy {} y tengo {} años.".format(nombre, edad))
# f-strings
print(f"hola, soy {nombre}, y mi edad es {edad} años")


